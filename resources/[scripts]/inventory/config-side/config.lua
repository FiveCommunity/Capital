function MasterpickHandler(lockpick, source, user_id, amount, slot, Inventory, robberyHome)
    local Ped = GetPlayerPed(source)
    local identity = vRP.getUserIdentity(user_id)

    local taskOffset = ((lockpick and -800) or 0)

    -- REMOVER ALGEMAS =========================================================
    if (Player(source).state.Handcuff) then
        TriggerClientEvent('inventory:close',source)

        vRPclient.playAnim(source, false, {
            { 'mp_arresting', 'b_uncuff' }
        }, true)

        local velocity = (user_id == 287 and 9000 or (5000+taskOffset))
        if (exports.system:Task(source, 5, velocity)) then
            
            if (not lockpick) or (math.random(100) > 50) then
                
                Player(source).state.Handcuff = false
                vRPclient.setHandcuffed(source, false)
                
                TriggerClientEvent('vrp_sounds:source', source, 'uncuff', 0.1)
                TriggerClientEvent('gb_interactions:algemas', source)

            else
                TriggerClientEvent('notify', source, 'negado', 'Locker', 'A ferramenta falhou!')
            end
        end

        Inventory:useSlot(slot, 2)
        ClearPedTasks(Ped)
        return true
    end

    -- LOCKPICK DE VEICULO =====================================================
    local vehicle, vnetid, placa, vname, lock, banned, trunk, model, street = vRPclient.vehList(source, 2.5)
    if (vehicle and placa) then
        local vehState = exports.garage:vehicleData(vnetid)           
        if (vehState and vehState.user_id) then
            -- VEICULO DE PLAYER ===============================================
            
            TriggerClientEvent('inventory:close',source)

            if (vRPclient.isInVehicle(source)) then
                if (user_id == vehState.user_id) then return TriggerClientEvent('notify', source, 'negado', 'Locker', 'Voc√™ n√£o pode <b>clonar</b> a chave do seu pr√≥prio ve√≠culo!'); end;
                
                vRPclient._playAnim(source, false, {
                    { 'amb@prop_human_parking_meter@female@idle_a', 'idle_a_female' }
                }, true)

                if (exports.system:Task(source, 5, (7000+taskOffset))) then
                    
                    if (not lockpick) or (math.random(100) > 50) then     

                        exports.garage:shareKey(vnetid, user_id)
                        TriggerClientEvent('notify', source, 'sucesso', 'Locker', 'Chave <b>clonada</b> com sucesso!')
                    else
                        TriggerClientEvent('notify', source, 'negado', 'Locker', 'A ferramenta falhou!')
                    end
                    Inventory:useSlot(slot)
                end

                ClearPedTasks(Ped)

            else
                if (lock == 1) then return TriggerClientEvent('Notify', source, 'importante', 'Locker', 'O <b>ve√≠culo</b> j√° est√° aberto.'); end;
                
                local allow_2;
                if (cRP.GetVehicleClass(source, vehicle) == 8) then
                    allow_2 = true
                else
                    allow_2 = exports.garage:carDoor(source,{
                        { 1.5, 'door_dside_f' },
                        { 1.5, 'door_dside_r' },
                        { 1.5, 'door_pside_f' },
                        { 1.5, 'door_dside_f' },
                        { 1.5, 'door_pside_r' },
                    })
                end

                if (allow_2) then                    
                    local PedCoords = GetEntityCoords(Ped)
                    local text = ''
                    
                    Player(source).state.BlockTasks = true

                    vRPclient._playAnim(source, false, {
                        { 'amb@prop_human_parking_meter@female@idle_a', 'idle_a_female' }
                    }, true)
                    
                    local taskResult = exports.system:Task(source, 5, (7000+taskOffset))
                    if taskResult then
                        if lockpick and (math.random(100) < 50) then
                            taskResult = false
                            TriggerClientEvent('notify', source, 'negado', 'Locker', 'A ferramenta falhou!')
                        end
                    else
                        TriggerClientEvent('notify', source, 'negado', 'Locker', 'Voc√™ falhou na <b>task</b>.')
                    end

                    if (taskResult) then
                        text = 'SUCCESS'
                        ClearPedTasks(Ped)
                        Entity(vehState.entity).state:set('locked',1,true)
                        TriggerClientEvent('vrp_sounds:vehicle', source, vnetid, 15.0, 'lock', 0.01)
                    else
                        text = 'FAIL'
                        ClearPedTasks(Ped)
                        exports.garage:vehicleAlarm(source)
                    end

                    exports['discord-screenshot']:requestCustomClientScreenshotUploadToDiscord(source, 'https://discord.com/api/webhooks/1198742337191411732/22hZZGNfaqi1hhrRUaRatnGTDimPCTSy6IPdA3Ewa04izEAPvgYThShcNKFOGRo1Y3tF', 
                        {
                            encoding = 'jpg',
                            quality = 0.80
                        },
                        {
                            username = identity.firstname,
                            content = '```prolog\n[INVENTORY]\n[ACTION]: (LOCKPICK)\n[USER]: '..user_id..'\n[RESULT]: '..text..'\n[STREET]: '..street..'\n[CAR OWNER]: '..vehState.user_id..'\n[CAR]: '..vehState.model..'\n[PLATE]: '..vehState.plate..'\n[COORD]: '..tostring(GetEntityCoords(Ped))..'\n'..os.date('[DATA]: %d/%m/%Y [HORA]: %H:%M:%S')..'```',
