                end

                Player(source).state.BlockTasks = false

                Inventory:useSlot(slot)

                return true
            end

        end
        return false
    end
    
    -- ROUBO RESIDENCIA ========================================================
    TriggerClientEvent('robbery:homes',source,slot)
end

