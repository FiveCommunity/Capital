                    if lockpick and (math.random(100) < 50) then
                        taskResult = false
                        TriggerClientEvent('notify', source, 'negado', 'Locker', 'A ferramenta falhou!')
                    end
                else
                    TriggerClientEvent('notify', source, 'negado', 'Locker', 'Voc√™ falhou na <b>task</b>.')
                end

                if (taskResult) then                            
                    Entity(vehState.entity).state:set('lockpicked',true,true)
                else
                    exports.garage:vehicleAlarm(source)
                end

                ClearPedTasks(Ped)

                TriggerClientEvent('Notify', source, 'importante', 'Locker', 'A sua tentativa de roubar um ve√≠culo foi denunciada para as autoridades!')
                
