config.extra = {
    ['telefone'] = function(source, user_id, amount, Inventory)
        local meta = {}

        local userHasNumber = exports.oxmysql:scalar_async('select phone_number from phone_phones where owner_id = ? and name is not null', { tostring ( user_id ) })
        if (userHasNumber) then
            local identity = vRP.getUserIdentity(user_id)

            meta.groupable = false
            meta.usages = 0
            meta.phoneNumber = userHasNumber
            meta.formattedNumber = exports['lb-phone']:FormatNumber(userHasNumber)
            meta.phoneName =  (identity and identity.firstname..' '..identity.lastname or 'Indivíduo Indigente')

            vRP.updateUserIdentity(user_id, 'phone', { phone = meta.formattedNumber })
        end

        if (exports['lb-phone']:updatePhone(user_id, meta)) then
            Inventory:giveItem('telefone', amount, nil, meta, true)
        end
    end
}

